const a = 100;
const b = 200;

// +, -, *, /, **, %
console.log("a + b = ", a + b);
console.log("a - b = ", a - b);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b);
console.log("2 ** 8 = ", 2 ** 8); //power (tavan)
console.log("251 % 2 = ", 251 % 2); //baghimande
console.log("10 - 5 * 4 = ", 10 - 5 * 4);
console.log("(10 - 5) * 4 = ", (10 - 5) * 4);

// ==, ===
console.log("100 == -100", 100 == -100);
console.log("100 == '100'", 100 == "100");
console.log("100 === '100'", 100 === "100");

//!= , !==
console.log("100 != 100", 100 != 100);

// >, <, >=, <==
console.log("10 > 4", 10 > 4);
console.log("10 >= 4", 10 >= 4);
console.log("20 <= 25", 20 <= 25);

const compare1 = 100 == 100;
const compare2 = "a" === "b";
const compare3 = "250" == 250;
console.log(compare1, compare2, compare3);
